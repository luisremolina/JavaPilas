    private void btnModificarConActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarConActionPerformed
        try {

            int busc = Integer.parseInt(txtCedula_Con.getText());

            nomb = txtNombre_Con.getText();
            apell = txtApellido_Con.getText();
            tel = txtTelefono_Con.getText();
            rh = txtRH_Con.getText();
            placa = txtPlaca.getText();
            num = Integer.parseInt(txtVehiculo_Con.getText());

            list_Con.modificar(busc, nomb, apell, tel, rh, placa, num);
            list_Con.guardar_datos();
            JOptionPane.showMessageDialog(null, "Se ha modificado el conductor correctamente");
            limpiar();
        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(null, "Por favor verifique los campos e intente nuevamente");

        }

    }//GEN-LAST:event_btnModificarConActionPerformed

    private void btnEliminarConActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarConActionPerformed
        try {
            int busc = Integer.parseInt(txtCedula_Con.getText());
            list_Con.eliminar(busc);
            list_Con.guardar_datos();
            limpiar();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Rellene correctamente el campo cedula");
        }
    }//GEN-LAST:event_btnEliminarConActionPerformed

