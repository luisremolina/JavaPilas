    private void btnBuscarConActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarConActionPerformed
        String recibidos[] = null;

        try {
            int busc = Integer.parseInt(txtCedula_Con.getText());
            recibidos = list_Con.buscar(busc);
            txtNombre_Con.setText(recibidos[0]);
            txtApellido_Con.setText(recibidos[1]);
            txtTelefono_Con.setText(recibidos[2]);
            txtRH_Con.setText(recibidos[3]);
            txtPlaca.setText(recibidos[4]);
            txtVehiculo_Con.setText(recibidos[5]);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Por favor rellene el campo cedula de manera correcta");
        }

    }//GEN-LAST:event_btnBuscarConActionPerformed

    private void btnGuardarConActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarConActionPerformed
        try {
            cedula = Integer.parseInt(txtCedula_Con.getText());

            nomb = txtNombre_Con.getText();
            apell = txtApellido_Con.getText();
            tel = txtTelefono_Con.getText();
            rh = txtRH_Con.getText();
            placa = txtPlaca.getText();

        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(null, "Por favor rellene el campo de cedula correctamente");

        }

        try {

            num = Integer.parseInt(txtVehiculo_Con.getText());

            list_Con.insertarcola(cedula, nomb, apell, tel, rh, placa, num);
            list_Con.guardar_datos();
            JOptionPane.showMessageDialog(null, "Se guardo el conductor con exito");
            limpiar();

        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(null, "Por favor rellene el campo del numero del vehiculo correctamente");

        }


    }//GEN-LAST:event_btnGuardarConActionPerformed

